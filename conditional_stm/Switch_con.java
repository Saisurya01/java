package conditional_stm;

public class Switch_con {
    public static void main(String[] args) {
        int a=2;
        switch (a) {
            case 1:
                System.out.println("one");
                break;
             case 2:
                System.out.println("two");
                break;
            default:
                System.out.println("not one or two");
                break;
        }
    }
}
