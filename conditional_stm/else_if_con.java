package conditional_stm;

public class else_if_con {
    public static void main(String[] args) {
        int a=10;
        if (a>0){
            System.out.println("a is positive number");
        }
        else if(a<0){
            System.out.println("a is negitive number");
        }
        else{
            System.out.println("a is 0");
        }
    }
}
